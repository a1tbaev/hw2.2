public class University {
    private String name;
    private String place;
    private int pupil;

    public void setName(String name) {
        this.name = name;
    }

    public String getName(){
        return name;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getPlace() {
        return place;
    }

    public void setPupil(int pupil) {
        this.pupil = pupil;
    }

    public int getPupil() {
        return pupil;
    }
}
